    private void displayGattServices(List<BluetoothGattService> gattServices) {
        if (gattServices == null) return;

        // Loops through available GATT Services.
        for (BluetoothGattService gattService : gattServices) {
            final String uuid = gattService.getUuid().toString();
            System.out.println("Service discovered: " + uuid);
            MainActivity.this.runOnUiThread(new Runnable() {
                public void run() {
                    peripheralTextView.append("Service disovered: "+uuid+"\n");
                }
            });
            new ArrayList<HashMap<String, String>>();
            List<BluetoothGattCharacteristic> gattCharacteristics =
                    gattService.getCharacteristics();

            // Loops through available Characteristics.
            for (BluetoothGattCharacteristic gattCharacteristic :
                    gattCharacteristics) {

                final String charUuid = gattCharacteristic.getUuid().toString();
//                final String val = gattCharacteristic.getValue().toString();
                System.out.println("Characteristic discovered for service: " + charUuid);
                //alternaitve uuid 5d1b1f74-ce22-11e8-a8d5-f2801f1b9fd1
                if (charUuid.equals("6e400003-b5a3-f393-e0a9-e50e24dcca9e")){
                    mNotifyCharacteristic1 = gattCharacteristic;
                }
                //alternaitve uuid 5d1b1b78-ce22-11e8-a8d5-f2801f1b9fd1
                if (charUuid.equals("6e400002-b5a3-f393-e0a9-e50e24dcca9e")){
                    mNotifyCharacteristic2 = gattCharacteristic;
                }
                MainActivity.this.runOnUiThread(new Runnable() {
                    public void run() {
                        peripheralTextView.append("Characteristic discovered for service: "+charUuid+"\n");
                    }
                });
            }

        }
    }

